export type ThemeKey = keyof typeof THEMES;

/** Returns the theme key for a given score (changes every 1000 points). */
export function getThemeForScore(score: number): ThemeKey {
  const keys = Object.keys(THEMES) as ThemeKey[];
  const idx = Math.min(Math.floor(score / 1000), keys.length - 1);
  return keys[idx];
}
